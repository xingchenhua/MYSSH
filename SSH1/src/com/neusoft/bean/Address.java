package com.neusoft.bean;

public class Address {
	private String recname;
	private String tel;
	private String adressInfact;

	public String getRecname() {
		return recname;
	}

	public void setRecname(String recname) {
		this.recname = recname;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getAdressInfact() {
		return adressInfact;
	}

	public void setAdressInfact(String adressInfact) {
		this.adressInfact = adressInfact;
	}
}
